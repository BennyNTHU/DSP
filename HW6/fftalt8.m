function X=fftalt8(x)

s1=x;
s2=x;
X=x;

for i=1:4
    s1(i)=x(i)+x(i+4);
end
for i=5:8
    s1(i)=x(i-4)-x(i);
end

s2(1)=s1(1)+s1(3);
s2(2)=s1(2)+s1(4);
s2(3)=s1(5)+exp(-j*2*pi*2/8)*s1(7);
s2(4)=s1(6)+exp(-j*2*pi*2/8)*s1(8);
s2(5)=s1(1)-s1(3);
s2(6)=s1(2)-s1(4);
s2(7)=s1(5)-exp(-j*2*pi*2/8)*s1(7);
s2(8)=s1(6)-exp(-j*2*pi*2/8)*s1(8);

X(1)=s2(1)+s2(2);
X(2)=s2(3)+exp(-j*2*pi/8)*s2(4);
X(3)=s2(5)+exp(-j*2*pi*2/8)*s2(6);
X(4)=s2(7)+exp(-j*2*pi*3/8)*s2(8);
X(5)=s2(1)-s2(2);
X(6)=s2(3)-exp(-j*2*pi/8)*s2(4);
X(7)=s2(5)-exp(-j*2*pi*2/8)*s2(6);
X(8)=s2(7)-exp(-j*2*pi*3/8)*s2(8);


